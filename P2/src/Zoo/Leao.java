package Zoo;

public class Leao implements Animal, Selvagem{

	String name, animalHabitat;
	int weight;
	
	
	public Leao(String n, int w){
		n = name;
		w = weight;
	}

	
	public void animalName(String n) {
		n = name;
	}
	
	
	public void animalWeight(int w) {
		w = weight;
		
	}


	public void setHabitat(String h) {
		h = animalHabitat;
		
	}

}
